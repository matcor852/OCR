int **read(const char *filename)
{
    int **array = malloc(9 * sizeof(int *));
    for (int i = 0; i < 9; i++)
        array[i] = (int *)malloc(9 * sizeof(int));
    if (!array)
        return NULL;
    FILE *file = fopen(filename, "r");
    if (!file)
        return NULL;
    size_t count;
    char buffer = '0';
    int value;
    size_t i = 0;
    size_t j = 0;

    do {
        count = fread(&buffer, 1, 1, file);
        if (buffer == '.')
            value = 0;
        else if (buffer >= '0' && buffer <= '9')
            value = atoi(&buffer);
        else
        {
            if (buffer == '\n' && j)
            {
                i++;
                j = 0;
            }
            continue; 
        }
        array[i][j] = value;
        j++;
    } while (count != 0);
    fclose(file);
    return array;
}


